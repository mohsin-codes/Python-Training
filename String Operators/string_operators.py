course = "Python for Beginners"

# Len Function
print(len(course))

# Upper Method
print(course.upper())

# Lower Method
print(course.lower())

# Title Method
print(course.title())

# Find Method
print(course.find("o"))
print(course.find("B"))
print(course.find("e"))

# Replace Method
print(course.replace("Beginners", "Absolute Beginners"))

# in keyword
print("Python" in course)