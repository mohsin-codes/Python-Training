person = ['john', 'bob', 'parker', 'Lucy', 'Jake']
#Printing the list
print(person)

#Printing list with index
print(person[1])
print(person[4])
print(person[3])

#Printing list with negative index
print(person[-1])
print(person[-2])

#Slicing through the list
print(person[2:])
print(person[2:4])
print(person[:3])
print(person[:])