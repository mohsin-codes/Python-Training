# iterating over a string
for item in "Python":
    print(item)

#iterating over a list
for item in ["Mohsin", "Ashar", "Zayn", "Samra"]:
    print(item)

# iterating over a range 
for item in range(10):
    print(item)

#iterating over a range with an initial value
for item in range(5,10):
    print(item)

#iterating over a range with an initial value with a step counter
for item in range(5,10,2):
    print(item)