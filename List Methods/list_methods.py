numbers = [1,2,3,1,3,4]

# Append Method
numbers.append(20)
print(numbers)

# Insert Method
numbers.insert(0, 10)
print(numbers)

# Remove Method
numbers.remove(3)
print(numbers)

# Pop Method
numbers.pop(4)
print(numbers)

# Index Method
print(numbers.index(1))

# Count Method
print(numbers.count(2))

# Sort Method
numbers.sort()
print(numbers)

# Reverse Method
numbers.reverse()
print(numbers)

# Copy Method
numbers2 = numbers.copy()
print(numbers2)

# Clear Method
numbers.clear()
print(numbers)